package logic_6;


public class ConditionalOperators {

    public static void main(String[] args) {

        // Оператр if
        int a = 4;
        if (a == 4) {
            System.out.println(a);
        }

        // Оператор if else
        if (a == 4) {
            System.out.println(a);
        } else {
            System.out.println("!!!!!!!!!");
        }

        // Оператор if else if  else
        if (a == 4) {
            System.out.println(a);
        } else if (a == 5) {
            System.out.println("!!!!");
        } else {
            System.out.println("!!!!!!!!!");
        }

    }

}
